#There are a number of functions built into Python that take lists as parameters;

nums = [3,41,12,9,74,15]
print(len(nums)) #the length of the nums in the list;
print(max(nums)) #the largest num in the list;
print(min(nums)) #the smallest num in the list;
print(sum(nums)) #sum of the nums;
print(sum(nums)/len(nums)) #Average of the nums;