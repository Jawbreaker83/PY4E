#Remember the 2nd number is up to but not including...;

t=[9,41,12, 3, 74,15]
t[1:3]
[41,12] #sub 1 up to but not including 3, which is in this case the number 3;
t[:4]
[9,41,12,3] #starting the beginning up to but not including #4 which in this case is the #74;
t[3:]
[3,74,15]
t[:]
[9,41,12, 3, 74,15]
